	void Update();

private:

	Input() {}
	Input(const Input&);
	Input& operator=(Input&);

	char m_key{ ' ' };

	int m_mouseWheel{ 0 };
	int m_modifier{ HM_MOD_NONE };
	int m_mouseButton{ HM_MOUSE_NONE };
